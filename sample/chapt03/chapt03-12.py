i = { 6: 1, 3: 2, 4: 3, 1: 4 }
print( i )    # {6: 1, 3: 2, 4: 3, 1: 4}　と表示される
print( i[6] )    # 1　と表示される
print( i[3] )    # 2　と表示される
j = { 'label_a': 1, 'label_b': 2, 'label_c': 3, 'label_d': 4 }
print( j )    # { 'label_a': 1, 'label_b': 2, 'label_c': 3, 'label_d': 4 }　と表示される
print( j[ 'label_a' ] )    # 1　と表示される
print( j[ 'label_b' ] )    # 2　と表示される
